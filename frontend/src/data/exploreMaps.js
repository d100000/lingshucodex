export function mapForLevel(level = 1) {
  const lv = Math.max(1, Number(level) || 1)
  return EXPLORE_MAPS.find(map => lv >= map.levelMin && lv <= map.levelMax)
    || EXPLORE_MAPS.reduce((best, map) => {
      const bestDistance = Math.min(Math.abs(lv - best.levelMin), Math.abs(lv - best.levelMax))
      const distance = Math.min(Math.abs(lv - map.levelMin), Math.abs(lv - map.levelMax))
      return distance < bestDistance ? map : best
    }, EXPLORE_MAPS[0])
}
